public class Pasager {
    private String nume;
    private String seriePasaport;

    public Pasager(String nume, String seriePasaport) {
        this.nume = nume;
        this.seriePasaport = seriePasaport;
    }

    public String getNume() {
        return nume;
    }

    public String getSeriePasaport() {
        return seriePasaport;
    }
}