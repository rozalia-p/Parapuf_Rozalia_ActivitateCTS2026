public class CerereCheltuiala {
    private String descriere;
    private double suma;

    public CerereCheltuiala(String descriere, double suma) {
        this.descriere = descriere;
        this.suma = suma;
    }

    public String getDescriere() {
        return descriere;
    }

    public double getSuma() {
        return suma;
    }
}